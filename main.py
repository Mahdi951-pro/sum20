# Initialize the starting point of the loop
i = 1

# Initialize the sum variable to store the cumulative sum
s = 0

# Define the number of natural numbers to sum up (from 1 to n)
n = 20

# Start the loop to sum up numbers from 1 to n
while i <= n:
    # Add the current value of i to the sum
    s = s + i
    
    # Increment i by 1 to move to the next natural number
    i = i + 1 

# After the loop ends, print the final sum
print("The sum is:", s)
